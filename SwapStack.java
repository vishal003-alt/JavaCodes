import java.util.Stack;
import java.util.Scanner;
public class SwapStack {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        Stack<Integer> st=new Stack<>();
        int num1=sc.nextInt();
        int num2=sc.nextInt();
        int num3=sc.nextInt();
        int num4=sc.nextInt();
        int num5=sc.nextInt();
        st.push(num1);
        st.push(num2);
        st.push(num3);
        st.push(num4);
        st.push(num5);
        


    }
    
}
